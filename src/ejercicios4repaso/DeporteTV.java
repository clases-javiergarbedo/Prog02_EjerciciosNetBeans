    private void jButtonMostrarTaquillaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMostrarTaquillaActionPerformed
        //Calcular el aforo total del estadio
        int aforoTribuna = Integer.valueOf(jTextFieldAforoTribuna.getText());
        int aforoPreferencia = Integer.valueOf(jTextFieldAforoPreferencia.getText());
        int aforoNorte = Integer.valueOf(jTextFieldAforoNorte.getText());
        int aforoSur = Integer.valueOf(jTextFieldAforoSur.getText());
        int aforoTotal = aforoTribuna + aforoPreferencia + aforoNorte + aforoSur;

        //Calular el número total de entradas vendidas
        int ventaTribuna = Integer.valueOf(jTextFieldVentaTribuna.getText());
        int ventaPreferencia = Integer.valueOf(jTextFieldVentaPreferencia.getText());
        int ventaNorte = Integer.valueOf(jTextFieldVentaNorte.getText());
        int ventaSur = Integer.valueOf(jTextFieldVentaSur.getText());
        int ventaTotal = ventaTribuna + ventaPreferencia + ventaNorte + ventaSur;

        //Obtener precios de cada categoría
        float precioTribuna = Float.valueOf(jTextFieldPrecioTribuna.getText());
        float precioPreferencia = Float.valueOf(jTextFieldPrecioPreferencia.getText());
        float precioNorte = Float.valueOf(jTextFieldPrecioNorte.getText());
        float precioSur = Float.valueOf(jTextFieldPrecioSur.getText());

        //Obtener porcentaje de aforo vendido
        float porcentajeAforoVendido = (float)ventaTotal / aforoTotal;
        float importeAforoVendido = ventaTribuna*precioTribuna + ventaPreferencia*precioPreferencia 
                + ventaNorte*precioNorte + ventaSur*precioSur;

        //Crear formatos de porcentaje y moneda según idioma seleccionado
        NumberFormat formatoPorcentaje = NumberFormat.getPercentInstance(jLocaleChooser1.getLocale());
        formatoPorcentaje.setMaximumFractionDigits(0);
        NumberFormat formatoMoneda = NumberFormat.getCurrencyInstance(jLocaleChooser1.getLocale());
        jLabelTVAbajo.setVisible(true);  
        jLabelTVAbajo.setText("Asistencia: " + formatoPorcentaje.format(porcentajeAforoVendido)
                + "    Taquilla: " + formatoMoneda.format(importeAforoVendido));  
    }//GEN-LAST:event_jButtonMostrarTaquillaActionPerformed

    private void jButtonPosesionEq1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPosesionEq1ActionPerformed
        //Calcular tiempo que ha durado la última posesión
        calendarInicioPosesionAnterior = calendarInicioPosesionActual;
        calendarInicioPosesionActual = Calendar.getInstance();
        long ultimoTiempoPosesion = calendarInicioPosesionActual.getTimeInMillis() - calendarInicioPosesionAnterior.getTimeInMillis();

        //Se aculuma el último tiempo de posesión al equipo que la tuviera
        tiempoPosesionEq2 += (equipoPosesion==2) ? ultimoTiempoPosesion : 0;

        //Se da la posesión al equipo 1
        equipoPosesion = 1;

        //Desactivar botón para que no se pueda repetir la pulsación
        jButtonPosesionEq1.setEnabled(false);
        jButtonPosesionEq2.setEnabled(true);  
        
        //Mostrar tiempo de posesión
        Calendar calendarMostrar = Calendar.getInstance();
        calendarMostrar.set(Calendar.HOUR_OF_DAY, 0); //Poner reloj a 0
        calendarMostrar.set(Calendar.MINUTE, 0);
        calendarMostrar.set(Calendar.SECOND, 0);
        //Sumar los segundos del tiempo al reloj en 0
        calendarMostrar.add(Calendar.SECOND, (int)tiempoPosesionEq2/1000);
        DateFormat formatoTiempo = DateFormat.getTimeInstance(DateFormat.MEDIUM, jLocaleChooser1.getLocale());
        jLabelPosesionEq2.setText(formatoTiempo.format(calendarMostrar.getTime()));
    }//GEN-LAST:event_jButtonPosesionEq1ActionPerformed

