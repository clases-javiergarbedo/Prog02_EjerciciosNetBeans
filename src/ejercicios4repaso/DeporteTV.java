    private void jButtonMostrarPosesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMostrarPosesionActionPerformed
        jLabelTVAbajo.setVisible(true);
        //Calcular porcentajes de posesión
        float porcenPosesionEq1 = (float)tiempoPosesionEq1 / (tiempoPosesionEq1 + tiempoPosesionEq2);
        float porcenPosesionEq2 = 1 - porcenPosesionEq1;
        //Crear formato de porcentaje
        NumberFormat formatoPorcentaje = NumberFormat.getPercentInstance(jLocaleChooser1.getLocale());
        //Mostrar datos de posesión de cada equipo
        String nombreEquipo1 = jTextFieldNombreEqLocal.getText();
        String nombreEquipo2 = jTextFieldNombreEqVisitante.getText();
        jLabelTVAbajo.setText("Posesión: " + nombreEquipo1 + " (" + formatoPorcentaje.format(porcenPosesionEq1) + ") "
                + nombreEquipo2 + " (" + formatoPorcentaje.format(porcenPosesionEq2) + ")");
    }//GEN-LAST:event_jButtonMostrarPosesionActionPerformed

