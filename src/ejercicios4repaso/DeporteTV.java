    private void jButtonGolActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGolActionPerformed
        Random generadorGoles = new Random();
        //Generar aletoriamente el equipo que marca el gol. Si el resultado
        //  es true se considera que ha marcado el equipo 1, y en caso contrario
        //  (false) se considera que ha marcado el equipo 2
        boolean golEquipo1 = generadorGoles.nextBoolean();
        //Incrementar el número de goles al equipo correspondiente
        numGoles1 += golEquipo1 ? 1 : 0;
        //Si no es gol del equipo1, se incrementa al equipo2
        numGoles2 += !golEquipo1 ? 1 : 0;
        //Mostrar el resultado actual
        jLabelMarcador.setText(numGoles1 + " - " + numGoles2);
    }//GEN-LAST:event_jButtonGolActionPerformed

